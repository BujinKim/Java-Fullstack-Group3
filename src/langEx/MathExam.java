package langEx;

public class MathExam {

	public static void main(String[] args) {
		
		System.out.println(Math.PI);//파이값 호출
		
		System.out.println(Math.floor(Math.PI)); //floor -> 소수점을 버림
		System.out.println(Math.ceil(Math.PI)); //ceil -> 소수점을 올림
		System.out.println(Math.max(3.14, 3.15)); // max -> 큰 값 호출
		System.out.println(Math.min(3.14, 3.15)); // min -> 작은 값 호출
		System.out.println(Math.random()); // random -> double이 허용하는 최대 소숫점의 모든 수에서 무작위로 나옴
		System.out.println(Math.round(3.14)); // round -> 반올림... 3 나옴
		System.out.println(Math.round(3.55)); // round -> 반올림... 4 나옴
		
		
		
		
	}

}
